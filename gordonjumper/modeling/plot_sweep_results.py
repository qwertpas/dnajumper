"""
Plot results from TSA optimization parameter sweeps.
Reads CSV files generated by optimal_comparison_plots.py and creates visualizations.
"""
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from pathlib import Path
import glob

# ============================================================================
# CONFIGURATION
# ============================================================================
# Set to True to display plots interactively, False to save to files
SHOW_PLOTS = True

# File patterns to find
# CSV_PATTERNS = [
#     "sweep_motor_0p5x.csv",
#     "sweep_motor_1p0x.csv",
#     "sweep_motor_2p0x.csv",
# ]

CSV_PATTERNS = [
    "sweep_motor_0p5x_80mm.csv",
    "sweep_motor_1p0x_80mm.csv",
    "sweep_motor_2p0x_80mm.csv",
]

# Colors for different motor scales
MOTOR_COLORS = {
    0.5: '#1f77b4',  # blue
    1.0: '#ff7f0e',  # orange
    2.0: '#2ca02c',  # green
}


def load_csv_files(directory="."):
    """Load all sweep CSV files."""
    files = {}
    for pattern in CSV_PATTERNS:
        path = Path(directory) / pattern
        if path.exists():
            scale = float(pattern.replace("sweep_motor_", "").replace("p", ".").replace("x.csv", ""))
            df = pd.read_csv(path)
            files[scale] = df
            print(f"Loaded {pattern}: {len(df)} rows, motor_scale={scale}")
    return files


def filter_successful(df):
    """Filter to only successful optimizations."""
    return df[df['success'] == True].copy()


def get_best_per_point(df, sweep_type):
    """Get best ydot_final for each unique parameter value."""
    if sweep_type == 'mass':
        group_col = 'm'
    else:  # inertia
        group_col = 'I_ratio'
    
    successful = filter_successful(df[df['sweep_type'] == sweep_type])
    if len(successful) == 0:
        return pd.DataFrame()
    
    best = successful.loc[successful.groupby(group_col)['ydot_final'].idxmax()]
    return best.sort_values(group_col)


def plot_mass_sweep(dataframes, ax=None):
    """Plot mass sweep results."""
    if ax is None:
        fig, ax = plt.subplots(figsize=(10, 6))
    
    for scale, df in sorted(dataframes.items()):
        best = get_best_per_point(df, 'mass')
        if len(best) > 0:
            ax.plot(best['m'] * 1000, best['ydot_final'], 
                   'o-', label=f'Motor {scale}x', 
                   color=MOTOR_COLORS.get(scale, 'gray'),
                   markersize=4, linewidth=1.5)
    
    ax.set_xlabel('Mass [g]', fontsize=12)
    ax.set_ylabel('Final Velocity ẏ [m/s]', fontsize=12)
    ax.set_title('Mass Sweep: Final Velocity vs Mass', fontsize=14, fontweight='bold')
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=10)
    return ax


def plot_inertia_sweep(dataframes, ax=None):
    """Plot inertia sweep results."""
    if ax is None:
        fig, ax = plt.subplots(figsize=(10, 6))
    
    for scale, df in sorted(dataframes.items()):
        best = get_best_per_point(df, 'inertia')
        if len(best) > 0:
            ax.semilogx(best['I_ratio'], best['ydot_final'],
                       'o-', label=f'Motor {scale}x',
                       color=MOTOR_COLORS.get(scale, 'gray'),
                       markersize=4, linewidth=1.5)
    
    ax.set_xlabel('Inertia Ratio I/I_base', fontsize=12)
    ax.set_ylabel('Final Velocity ẏ [m/s]', fontsize=12)
    ax.set_title('Inertia Sweep: Final Velocity vs Inertia Ratio', fontsize=14, fontweight='bold')
    ax.grid(True, alpha=0.3, which='both')
    ax.legend(fontsize=10)
    return ax


def plot_optimized_parameters(dataframes, sweep_type='mass', axs=None):
    """Plot optimized L, r, T vs parameter."""
    if axs is None:
        fig, axs = plt.subplots(3, 1, figsize=(10, 12))
    
    if sweep_type == 'mass':
        x_col = 'm'
        x_label = 'Mass [g]'
        x_scale = 1000
    else:
        x_col = 'I_ratio'
        x_label = 'Inertia Ratio I/I_base'
        x_scale = 1
    
    for scale, df in sorted(dataframes.items()):
        best = get_best_per_point(df, sweep_type)
        if len(best) > 0:
            x = best[x_col] * x_scale if x_scale != 1 else best[x_col]
            color = MOTOR_COLORS.get(scale, 'gray')
            label = f'Motor {scale}x'
            
            axs[0].plot(x, best['opt_L'] * 1000, 'o-', label=label, 
                       color=color, markersize=4, linewidth=1.5)
            axs[1].plot(x, best['opt_r'] * 1000, 'o-', label=label,
                       color=color, markersize=4, linewidth=1.5)
            axs[2].plot(x, best['opt_T'], 'o-', label=label,
                       color=color, markersize=4, linewidth=1.5)
    
    axs[0].set_ylabel('Optimal L [mm]', fontsize=11)
    axs[0].set_title(f'{sweep_type.capitalize()} Sweep: Optimized Parameters', 
                     fontsize=14, fontweight='bold')
    axs[0].grid(True, alpha=0.3)
    axs[0].legend(fontsize=9)
    
    axs[1].set_ylabel('Optimal r [mm]', fontsize=11)
    axs[1].grid(True, alpha=0.3)
    
    axs[2].set_xlabel(x_label, fontsize=11)
    axs[2].set_ylabel('Optimal T [s]', fontsize=11)
    axs[2].grid(True, alpha=0.3)
    
    if sweep_type == 'inertia':
        axs[0].semilogx()
        axs[1].semilogx()
        axs[2].semilogx()
    
    return axs


def plot_success_rate(dataframes, ax=None):
    """Plot success rate for each motor scale."""
    if ax is None:
        fig, ax = plt.subplots(figsize=(10, 6))
    
    scales = []
    mass_success = []
    inertia_success = []
    
    for scale, df in sorted(dataframes.items()):
        scales.append(scale)
        
        mass_df = df[df['sweep_type'] == 'mass']
        mass_total = len(mass_df.groupby('m'))
        mass_success_count = len(filter_successful(mass_df).groupby('m'))
        mass_success.append(mass_success_count / mass_total * 100 if mass_total > 0 else 0)
        
        inertia_df = df[df['sweep_type'] == 'inertia']
        inertia_total = len(inertia_df.groupby('I_ratio'))
        inertia_success_count = len(filter_successful(inertia_df).groupby('I_ratio'))
        inertia_success.append(inertia_success_count / inertia_total * 100 if inertia_total > 0 else 0)
    
    x = np.arange(len(scales))
    width = 0.35
    
    ax.bar(x - width/2, mass_success, width, label='Mass Sweep', alpha=0.8)
    ax.bar(x + width/2, inertia_success, width, label='Inertia Sweep', alpha=0.8)
    
    ax.set_xlabel('Motor Scale', fontsize=12)
    ax.set_ylabel('Success Rate [%]', fontsize=12)
    ax.set_title('Optimization Success Rate by Motor Scale', fontsize=14, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels([f'{s}x' for s in scales])
    ax.set_ylim([0, 105])
    ax.grid(True, alpha=0.3, axis='y')
    ax.legend(fontsize=10)
    
    return ax


def save_or_show(fig, filename=None, save_dir="."):
    """Save plot to file or show interactively based on SHOW_PLOTS setting."""
    plt.tight_layout()
    if SHOW_PLOTS:
        if filename:
            print(f"  Displaying: {filename}")
        plt.show(block=False)
    else:
        if filename:
            save_path = Path(save_dir) / filename
            plt.savefig(save_path, dpi=150, bbox_inches='tight')
            print(f"  Saved: {filename}")
        plt.close(fig)


def create_all_plots(dataframes, save_dir="."):
    """Create all plots and save or show them based on SHOW_PLOTS setting."""
    print("\nGenerating plots...")
    if SHOW_PLOTS:
        print("  Mode: Display plots interactively")
    else:
        print("  Mode: Save plots to files")
    
    # 1. Mass sweep - final velocity
    fig, ax = plt.subplots(figsize=(10, 6))
    plot_mass_sweep(dataframes, ax)
    save_or_show(fig, 'mass_sweep_velocity.png', save_dir)
    
    # 2. Inertia sweep - final velocity
    fig, ax = plt.subplots(figsize=(10, 6))
    plot_inertia_sweep(dataframes, ax)
    save_or_show(fig, 'inertia_sweep_velocity.png', save_dir)
    
    # 3. Mass sweep - optimized parameters
    fig, axs = plt.subplots(3, 1, figsize=(10, 12))
    plot_optimized_parameters(dataframes, 'mass', axs)
    save_or_show(fig, 'mass_sweep_parameters.png', save_dir)
    
    # 4. Inertia sweep - optimized parameters
    fig, axs = plt.subplots(3, 1, figsize=(10, 12))
    plot_optimized_parameters(dataframes, 'inertia', axs)
    save_or_show(fig, 'inertia_sweep_parameters.png', save_dir)
    
    # 5. Success rate
    fig, ax = plt.subplots(figsize=(10, 6))
    plot_success_rate(dataframes, ax)
    save_or_show(fig, 'success_rate.png', save_dir)
    
    # 6. Combined comparison plot
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    
    plot_mass_sweep(dataframes, axes[0, 0])
    plot_inertia_sweep(dataframes, axes[0, 1])
    plot_success_rate(dataframes, axes[1, 0])
    
    # Summary statistics table
    axes[1, 1].axis('off')
    summary_text = []
    for scale, df in sorted(dataframes.items()):
        successful = filter_successful(df)
        if len(successful) > 0:
            max_ydot = successful['ydot_final'].max()
            avg_ydot = successful['ydot_final'].mean()
            summary_text.append(f"Motor {scale}x:")
            summary_text.append(f"  Max ẏ: {max_ydot:.3f} m/s")
            summary_text.append(f"  Avg ẏ: {avg_ydot:.3f} m/s")
            summary_text.append(f"  Success: {len(successful)}/{len(df)}")
            summary_text.append("")
    
    axes[1, 1].text(0.1, 0.5, '\n'.join(summary_text), 
                    fontsize=11, family='monospace',
                    verticalalignment='center')
    axes[1, 1].set_title('Summary Statistics', fontsize=12, fontweight='bold')
    
    save_or_show(fig, 'sweep_comparison.png', save_dir)
    
    if SHOW_PLOTS:
        print("\nAll plots displayed! Close windows to continue.")
        plt.show(block=True)  # Block until all windows are closed
    else:
        print("\nAll plots generated successfully!")


if __name__ == "__main__":
    import sys
    
    # Get directory from command line or use current directory
    if len(sys.argv) > 1:
        directory = sys.argv[1]
    else:
        directory = "."
    
    print("="*70)
    print("TSA Optimization Sweep Results Plotter")
    print("="*70)
    
    # Load CSV files
    dataframes = load_csv_files(directory)
    
    if len(dataframes) == 0:
        print("\nERROR: No CSV files found!")
        print(f"Looking for: {CSV_PATTERNS}")
        sys.exit(1)
    
    # Create all plots
    create_all_plots(dataframes, directory)
    
    print("\n" + "="*70)
    print("Done!")
    print("="*70)

